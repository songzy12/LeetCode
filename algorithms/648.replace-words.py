# prefix tree

class Node(object):
    def __init__(self):
        self.chars = {}
        self.valid = False
        self.word = ""

class Solution(object):
    def replaceWords(self, dict, sentence):
        """
        :type dict: List[str]
        :type sentence: str
        :rtype: str
        """

        def build_tree():
            root = Node()
            for word in dict:
                cur = root
                for c in word:
                    if c not in cur.chars:
                        cur.chars[c] = Node()
                        cur.chars[c].word = cur.word + c
                    cur = cur.chars[c]
                    
                cur.valid = True
            return root

        root = build_tree()

        def search_tree(word):
            cur = root
            
            for c in word:
                if c not in cur.chars:
                    return word
                cur = cur.chars[c]
                if cur.valid:
                    return cur.word
            return word
                    

        ans = [search_tree(x) for x in sentence.split()]
        return ' '.join(ans)

